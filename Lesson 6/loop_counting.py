# Goal:  Count up to a predefined number

# Initialize the Loop Control Variable
# i is keeping track of how many times the loop has executed
i = 0

# Test:  Keep running until i >= 10
while i < 10:
    
    # Execute:  just printing out i to prove that this loop is executing 10 times
    print(i)
    
    # Modify:  Incrementing i, since it keeps track of how many times the loop has executed
    i = i + 1
    
# A simple print statement to let you know when you leave the loop
print("Loop Complete")
    
    