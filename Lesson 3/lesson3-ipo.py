# Example Input-Process-Output Problem
# Problem:  Write an algorithm that gets two numbers from the
# user, calculates the average, and outputs the result.

# Step 1:  Get the values
value1 = int(input())
value2 = int(input())

# Step 2:  Process the values
average = (value1 + value2) / 2

# Step 3:  Print the Result
print(average)